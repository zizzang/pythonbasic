# while 문의 실습
# while 문은 조건을 정해놓고 반복을 하는 구조이다.

from ast import Num


i = 0
while i < 5:
    print("반갑습니다.")
    i += 1      # 만약 좌측 코드가 없다면, 무한루프에 빠지게 된다.
print("반복이 종료되었습니다.")
