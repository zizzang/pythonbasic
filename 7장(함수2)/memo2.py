# 아래도 숙지
#  함수 안에서 변수에 어떤 값을 저장하면 무조건 지역변수로 간주한다. 디폴트가 지역변수인 것이다.

def sub():
    s = "바나나"
    print(s)

s = "사과"
sub()
print(s)


# 지역 변수에 전역 변수의 값을 사용하려면
#  globla s 를 써준다

def sub():
    global s
    print(s)
    s = "바나나가 좋음!"
    print(s)

s = "사과가 좋음!!"
sub()
print(s)
