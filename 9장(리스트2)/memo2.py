# "값으로 호출하기" 와 "참조로 호출하기"

# 1. "값으로 호출하기(Call-by-Value)" : 함수로 변수를 전달할 때, 변수의 값이 복사되는 방식으로 가장많이 사용되는 방법
# 변경 가능한 객체이냐 > 리스트, 딕셔너리

# 2. "참조로 호출하기(Call-by-Reference)" : 함수로 변수를 전달할 때, 변수의 참조가 전달되는 방법. 함수에서는 매개변수를 
#                                          통하여 원본 변수를 변경할 수 있다.
# 변경불가능한 객체이냐 > 정수형, 문자열, 튜플
