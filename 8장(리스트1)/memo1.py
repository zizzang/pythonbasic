# 리스트

# 쉽게 대량의 데이터를 저장할 수 있는 공간을 만들 수 있어야 하고 이 데이터들을 손쉽게 처리할 수
# 있는 방법이필요하다. 그래서 있는것이 리스트이다.
# 리스트는 하나의 이름을 공유한다.
# 사용자로부터 값을 받아서 리스트에 추가하려면 append() 메소드를 사용한다.
# 파이썬의 리스트는 다른 언어의 배열(array)와 유사하다.
# BUT 배열이 크기는 고정되어 있는 반연에 리스트의 크기는 가변적이다.
# AND 배열은 같은 데이터 타입만 저장할 수 있는 반면에 리스트는 다양한 데이터 타입 저장 가능하다.
# 리스트에 저장된 데이터들을 리스트 요소(array element)라고 한다.
# 리스트의 요소에는 번호가 붙어있는데 이것을 인덱스(index) = 첨자 라고 칭한다.
# 유요한 인덱스의 범위는 0에서 (리스트 크기 -1)까지이다.